CREATE TABLE USUARIO (
    ID NUMBER(*, 0) GENERATED ALWAYS AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE NOT NULL ENABLE,
    NAME VARCHAR2(128),
    PASSWORD VARCHAR2(512 CHAR),
    EMAIL VARCHAR2(512 CHAR),
    CPF VARCHAR2(512 CHAR),
    PHONE VARCHAR2(512 CHAR),
    BIRTH VARCHAR2(512 CHAR),
    PRIMARY KEY (ID)
);

/

CREATE TABLE TRIAGEM (
    ID NUMBER(*, 0) GENERATED ALWAYS AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE NOT NULL ENABLE,
    STATUS VARCHAR2 (128 CHAR),
    SYMPTOMS VARCHAR2(512 CHAR),
    PAIN_LEVEL INTEGER,
    TAKING_MEDICATION VARCHAR2(512 CHAR),
    BREATHING VARCHAR2(512 CHAR),
    FEVER VARCHAR2(128 CHAR),
    TEMPERATURE VARCHAR2(128 CHAR),
    PRESSURE VARCHAR2(128 CHAR),
    USER_ID INTEGER,
    PRIMARY KEY (ID),
    FOREIGN KEY(USER_ID) REFERENCES USUARIO(ID)
);

/

